namespace theRightDirection.Enumerations
{
    /// <summary>
    /// Defines the enumeration values for a day's current state.
    /// </summary>
    public enum StateOfDay
    {
        /// <summary>
        /// Morning, generally between 6AM and 12AM.
        /// </summary>
        Morning,

        /// <summary>
        /// Afternoon, generally between 12AM and 6PM.
        /// </summary>
        Afternoon,

        /// <summary>
        /// Evening, generally between 6PM and 6AM.
        /// </summary>
        Evening
    }
}